/* Build a 32768-entry LUT: RGB555 -> nearest palette index (squared-RGB
 * distance), same as legacy gdi::GetConvertTable(15, 8) in
 * RC5XWIN/lib_gdi/ColorConv.cpp. Called once at encoder init (~few ms). */
static void ScapBuildLut(uint8_t lut[32768])
{
    for (int c = 0; c < 32768; ++c)
    {
        /* legacy C15_RGB: R = bits 10-14, G = bits 5-9, B = bits 0-4 */
        int r = (c >> 7) & 0xF8, g = (c >> 2) & 0xF8, b = (c << 3) & 0xF8;
        uint32_t best = 0xFFFFFFFF;
        int bestIdx = 0;
        for (int i = 0; i < SCAP_PAL_USED; ++i)
        {
            int dr = r - kScapPal[i].r, dg = g - kScapPal[i].g, db = b - kScapPal[i].b;
            uint32_t d = (uint32_t)(dr*dr + dg*dg + db*db);
            if (d < best) { best = d; bestIdx = i; if (d == 0) break; }
        }
        lut[c] = (uint8_t)bestIdx;
    }
}

/* legacy C32_15: pack a BGRA32 pixel (bytes B,G,R,A) into RGB555 */
static __inline unsigned ScapC32_15(const uint8_t* p)
{
    return ((unsigned)(p[2] & 0xF8) << 7) | ((unsigned)(p[1] & 0xF8) << 2) | ((unsigned)p[0] >> 3);
}
